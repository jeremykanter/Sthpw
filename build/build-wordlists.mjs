#!/usr/bin/env node
// Build step: partition an English dictionary into "left-hand only" and
// "right-hand only" words and emit a compact wordlists.js that the app loads
// directly via a <script> tag (no fetch / no server / no CORS).
//
// Usage:
//   node build/build-wordlists.mjs
//
// Source dictionary (downloaded automatically if missing):
//   https://raw.githubusercontent.com/dwyl/english-words/master/words_alpha.txt
//
// This is a one-time dev convenience. The generated wordlists.js is committed,
// so end users never run this.

import { readFile, writeFile, access } from "node:fs/promises";
import { constants } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, "..");

const SOURCE_URL =
  "https://raw.githubusercontent.com/dwyl/english-words/master/words_alpha.txt";
const SOURCE_PATH = join(__dirname, "words_alpha.txt");
const OUT_PATH = join(ROOT, "wordlists.js");

// Standard touch-typing letter split.
const LEFT_LETTERS = new Set("qwertasdfgzxcvb");
const RIGHT_LETTERS = new Set("yuiophjklnm");

// "Balanced" (fast-typing) words for Fstbll mode: neither hand types more than
// this share of the letters, so the hands keep alternating.
const MAX_HAND_SHARE = 0.6;

// Generous length window kept in the bundled data; the UI filters within it.
const MIN_LEN = 3;
const MAX_LEN = 12;

async function fileExists(p) {
  try {
    await access(p, constants.R_OK);
    return true;
  } catch {
    return false;
  }
}

async function ensureSource() {
  if (await fileExists(SOURCE_PATH)) return;
  console.log(`Downloading dictionary from ${SOURCE_URL} ...`);
  const res = await fetch(SOURCE_URL);
  if (!res.ok) {
    throw new Error(`Failed to download dictionary: HTTP ${res.status}`);
  }
  const text = await res.text();
  await writeFile(SOURCE_PATH, text, "utf8");
  console.log(`Saved ${SOURCE_PATH} (${(text.length / 1e6).toFixed(1)} MB)`);
}

function allLettersIn(word, set) {
  for (const ch of word) {
    if (!set.has(ch)) return false;
  }
  return true;
}

function isBalanced(word) {
  let left = 0;
  for (const ch of word) if (LEFT_LETTERS.has(ch)) left++;
  const right = word.length - left; // every QWERTY letter is left or right
  return (
    left / word.length <= MAX_HAND_SHARE &&
    right / word.length <= MAX_HAND_SHARE
  );
}

async function main() {
  await ensureSource();

  const raw = await readFile(SOURCE_PATH, "utf8");
  const words = raw.split(/\r?\n/);

  const left = [];
  const right = [];
  const balanced = [];
  const seenLeft = new Set();
  const seenRight = new Set();
  const seenBalanced = new Set();

  for (const w0 of words) {
    const w = w0.trim().toLowerCase();
    if (w.length < MIN_LEN || w.length > MAX_LEN) continue;
    if (!/^[a-z]+$/.test(w)) continue;

    if (allLettersIn(w, LEFT_LETTERS)) {
      if (!seenLeft.has(w)) {
        seenLeft.add(w);
        left.push(w);
      }
    } else if (allLettersIn(w, RIGHT_LETTERS)) {
      if (!seenRight.has(w)) {
        seenRight.add(w);
        right.push(w);
      }
    }

    // Balanced words are mixed-hand, so they never overlap left/right above.
    if (isBalanced(w) && !seenBalanced.has(w)) {
      seenBalanced.add(w);
      balanced.push(w);
    }
  }

  left.sort();
  right.sort();
  balanced.sort();

  const header =
    "// AUTO-GENERATED by build/build-wordlists.mjs — do not edit by hand.\n" +
    `// Source: dwyl/english-words. Length window: ${MIN_LEN}-${MAX_LEN}.\n` +
    `// LEFT_WORDS: ${left.length}, RIGHT_WORDS: ${right.length}, ` +
    `BALANCED_WORDS: ${balanced.length} (each hand ${Math.round((1 - MAX_HAND_SHARE) * 100)}-${Math.round(MAX_HAND_SHARE * 100)}%).\n`;

  const body =
    `const LEFT_WORDS = ${JSON.stringify(left.join(" "))}.split(" ");\n` +
    `const RIGHT_WORDS = ${JSON.stringify(right.join(" "))}.split(" ");\n` +
    `const BALANCED_WORDS = ${JSON.stringify(balanced.join(" "))}.split(" ");\n`;

  await writeFile(OUT_PATH, header + body, "utf8");

  // Sanity report.
  const sampleRight = ["you", "pony", "milk", "onion", "nylon"].filter((w) =>
    seenRight.has(w)
  );
  console.log(`Wrote ${OUT_PATH}`);
  console.log(`  LEFT_WORDS:     ${left.length}`);
  console.log(`  RIGHT_WORDS:    ${right.length}`);
  console.log(`  BALANCED_WORDS: ${balanced.length}`);
  console.log(`  right-word sanity hits: ${sampleRight.join(", ") || "(none!)"}`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
